const alunos = ['João', 'Gabriel', 'Maria'];

/* push - Método que ADICIONA um valor ao FINAL da lista */
alunos.push('Lany'); 

console.log(alunos);

/* pop - Método que DELETA um valor ao FINAL da lista */
alunos.pop(); 

console.log(alunos);

/* shift - Método que DELETA um valor no INÍCIO da lista */
alunos.shift(); 

console.log(alunos);

/* length - Quantidade de elementos na lista */ 
console.log(alunos.length);

